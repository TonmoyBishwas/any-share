// Minimal service worker: makes the app installable and works offline.
// Network-first so users always get the latest version when online; falls back
// to cache (and the app shell) when offline. WebSocket upgrades aren't GETs, so
// signaling traffic is never touched here.
const CACHE = "any-share-v1";

self.addEventListener("install", () => self.skipWaiting());
self.addEventListener("activate", (e) => e.waitUntil(self.clients.claim()));

self.addEventListener("fetch", (e) => {
  const req = e.request;
  if (req.method !== "GET") return;
  e.respondWith(
    fetch(req)
      .then((res) => {
        const copy = res.clone();
        caches.open(CACHE).then((c) => c.put(req, copy)).catch(() => {});
        return res;
      })
      .catch(() => caches.match(req).then((r) => r || caches.match("/")))
  );
});
